package Exercicio10;

public class Ponto {
	
	private double x;
	private double y;
	
	public Ponto (double x, double y) {
		this.x=x;
		this.y=y;
		
	}
	public Ponto() {
	}
	public double getx() {
		return x;
	}
	public void setx(double x) {
		this.x=x;
		
	}
	public double gety() {
		return y;
		
	}
	public void sety(double y) {
		this.y=y;
	}
}
